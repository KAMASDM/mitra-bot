// Helper functions
export const getResourcesByCategory = (category) => {
  return RESOURCES_DATA.filter(resource => resource.category === category);
};

export const searchResources = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return RESOURCES_DATA.filter(resource => 
    resource.name.toLowerCase().includes(lowercaseQuery) ||
    resource.description.toLowerCase().includes(lowercaseQuery) ||
    resource.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getResourcesForUser = (userType) => {
  // Filter resources based on user type (LGBTQAI+, Women, etc.)
  return RESOURCES_DATA.filter(resource => 
    resource.availableFor.includes(userType) || 
    resource.availableFor.includes('All')
  );
};
